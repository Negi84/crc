                    <div class="accordion-body">
                        <div class="text-238095">
                        Climate change is a significant threat not only to human health but also to animal and plant health. While a changing climate might not create many new or unknown health threats, existing effects will be exacerbated and more pronounced than currently seen.
                        The climate crisis is already affecting every inhabited region of the globe, with dire consequences for individuals and global public health such as:
                        <ul>
                            <li>Rising cases of malnutrition due to soil and drinking water salinization and pollution, land degradation, reduced viability of crops, desertification, and other slow consequences.</li>
                            <li>More frequent and more severe breathing conditions due to rising air pollution;</li>
                            <li>A greater incidence of water-borne diseases such as cholera and typhoid due to increased flooding;</li>
                            <li>Weakened and overstretched health systems as storms, heavy precipitations, floods, heat waves, droughts and other extreme weather events become more frequent and people's health needs increase, particularly in low- and middle-income countries.</li>
                        </ul>
                        </div>
                        <div class="text-238095">
                        The IPPC revealed that rapidly increasing climate change poses a rising threat to mental health and psychosocial well-being; from emotional distress to anxiety, depression, grief, and suicidal behavior.
                        “The impacts of climate change are increasingly part of our daily lives, and there is very little dedicated mental health support available for people and communities dealing with climate-related hazards and long-term risk,” said Dr Maria Neira, Director of the Department of Environment, Climate Change and Health at WHO.
