                            <p class="py-2">
                            People living in low-income urban areas with poor infrastructure, and, generally speaking, population groups with lower incomes and assets, are more exposed to climate impacts but have less capacity to face them. Women may be disproportionately impacted by climate change and are at a disadvantage
                            when expensive adaptation measures are required. At the same time, women are key actors in adaptation and more generally sustainable practices. Unemployed and socially marginalised people are among the most vulnerable to climate risks.
                            </p>
                            <p class="">
                            Climate change and climate variability are projected to have a substantial effect on agricultural production, both regarding crop yields and the locations where different crops can be grown. Effects on forestry due to climate change include increased risk of droughts, storms and fires (abiotic) and pests and diseases (biotic) – all leading to disturbances to forest health.
                            The economic consequences of climate change for regions where tourism is important can be substantial. Projected reductions in snow cover will negatively affect the winter sports industry in many regions
